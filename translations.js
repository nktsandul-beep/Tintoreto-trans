// Language switching function
function switchLanguage(lang) {
  // Save language preference
  localStorage.setItem('preferred-language', lang);
  
  // Update all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang] && translations[lang][key]) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
  
  // Update language switcher current button
  const langCurrentText = document.getElementById('lang-current-text');
  if (langCurrentText) {
    langCurrentText.textContent = lang.toUpperCase();
  }
  
  // Update language dropdown options
  document.querySelectorAll('.lang-option').forEach(option => {
    option.classList.remove('active');
    if (option.getAttribute('data-lang') === lang) {
      option.classList.add('active');
    }
  });
  
  // Close dropdown
  const langSwitcher = document.querySelector('.lang-switcher');
  if (langSwitcher) {
    langSwitcher.classList.remove('active');
  }
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  // Get saved language or default to Russian
  const savedLang = localStorage.getItem('preferred-language') || 'ru';
  switchLanguage(savedLang);
  
  // Toggle dropdown on current button click
  const langCurrent = document.getElementById('lang-current');
  const langSwitcher = document.querySelector('.lang-switcher');
  
  if (langCurrent && langSwitcher) {
    langCurrent.addEventListener('click', function(e) {
      e.stopPropagation();
      langSwitcher.classList.toggle('active');
    });
  }
  
  // Add click handlers to language options
  document.querySelectorAll('.lang-option').forEach(option => {
    option.addEventListener('click', function(e) {
      e.stopPropagation();
      const lang = this.getAttribute('data-lang');
      switchLanguage(lang);
    });
  });
  
  // Close dropdown when clicking outside
  document.addEventListener('click', function(e) {
    if (langSwitcher && !langSwitcher.contains(e.target)) {
      langSwitcher.classList.remove('active');
    }
  });
});

